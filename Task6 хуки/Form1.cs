using System;                    // Базовые типы: Action, EventArgs и др.
using System.Windows.Forms;      // Формы, кнопки, метки — весь UI
using HookLibrary;               // НАША DLL Подключаем пространство имён с хуком

// ОБЪЯВЛЕНИЕ ПРОСТРАНСТВА ИМЁН И КЛАССА ФОРМЫ
namespace Task6_dll            // Имя проекта (управляющее приложение)
{
    // Form1 наследуется от Form — это окно Windows Forms
    public partial class Form1 : Form
    {
        // ПОЛЯ КЛАССА (переменные, доступные всем методам формы)

        // Ссылка на объект хука из DLL
        // "Где хранится ловушка?" → в поле _hook
        private GlobalHook _hook;

        // Счётчик перехваченных клавиш
        private int _keyCount = 0;

        // КОНСТРУКТОР — выполняется при создании формы
        public Form1()
        {
            // Инициализация элементов формы (кнопок, меток) — создаётся в Designer
            InitializeComponent();

            // ЗАГРУЗКА ЛОВУШКИ
            // Создаём экземпляр класса GlobalHook из DLL HookLibrary
            // Это и есть "загрузка ловушки" — объект начинает существовать в памяти
            _hook = new GlobalHook();

            // Подписываемся на событие KeyCaught — когда хук "поймает" клавишу,
            // будет вызван метод OnKeyCaught для обработки данных
            _hook.KeyCaught += OnKeyCaught;
        }

        // ОБРАБОТЧИК СОБЫТИЯ: получение данных от ловушки
        /// Вызывается автоматически, когда хук перехватывает нажатие клавиши.
        /// Параметры:
        ///   - vkCode: виртуальный код клавиши (число, например 65 = 'A')
        ///   - keyName: читаемое имя клавиши (строка "A", "Enter" и т.д.)
        private void OnKeyCaught(int vkCode, string keyName)
        {
            // Увеличиваем счётчик перехваченных клавиш
            _keyCount++;

            // ВАЖНО: обновление UI из другого потока
            // Хук работает в системном потоке, а форма — в главном потоке приложения.
            // Прямое изменение lblInfo из другого потока вызовет ошибку.
            // BeginInvoke выполняет код в потоке UI безопасно.
            this.BeginInvoke(new Action(() =>
            {
                // Обновляем текст метки с информацией
                lblInfo.Text = $"Перехвачено клавиш: {_keyCount}\nПоследняя: {keyName}";
            }));
        }

        // ОБРАБОТЧИК КНОПКИ: установка хука
        /// Вызывается при нажатии кнопки "Установить хук"
        private void btnInstall_Click(object sender, EventArgs e)
        {
            // Вызываем метод Install() из DLL — хук регистрируется в системе
            // и начинает перехватывать нажатия клавиш глобально
            _hook.Install();

            // Обновляем статус для пользователя
            lblStatus.Text = "Статус: Хук установлен";

            // "Почему хук глобальный?" - используем WH_KEYBOARD_LL (low-level), который работает
            // на уровне системы и перехватывает ввод до передачи в приложения.
        }

        // ОБРАБОТЧИК КНОПКИ: выгрузка хука 
        /// Вызывается при нажатии кнопки "Удалить хук"
        private void btnUninstall_Click(object sender, EventArgs e)
        {
            // Вызываем метод Uninstall() — хук удаляется из системы,
            // перехват прекращается, ресурсы освобождаются
            _hook.Uninstall();

            lblStatus.Text = "Статус: Хук удален";

            // "Что будет, если не удалить хук?" - утечка ресурсов, хук останется в системе, возможны сбои
        }

        // ЗАЩИТА: гарантированная выгрузка при закрытии программы
        /// Переопределённый метод — вызывается автоматически при закрытии формы.
        /// Гарантирует, что хук будет удалён, даже если пользователь нажмёт крестик
        protected override void OnFormClosing(FormClosingEventArgs e)
        {
            // Если хук активен — обязательно удаляем его
            if (_hook != null)
            {
                _hook.Uninstall();
            }

            // Вызываем базовую реализацию (обязательно)
            base.OnFormClosing(e);
        }
    }
}